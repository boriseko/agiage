// Helpers
function heading1(text) {
  return new Paragraph({
    spacing: { before: 400, after: 200 },
    children: [new TextRun({ text, bold: true, size: 36, font: "Arial", color: DARK })]
  });
}

function heading2(text) {
  return new Paragraph({
    spacing: { before: 300, after: 150 },
    children: [new TextRun({ text, bold: true, size: 28, font: "Arial", color: DARK })]
  });
}

function heading3(text) {
  return new Paragraph({
    spacing: { before: 200, after: 100 },
    children: [new TextRun({ text, bold: true, size: 24, font: "Arial", color: GOLD })]
  });
}

function label(text) {
  return new Paragraph({
    spacing: { before: 300, after: 60 },
    children: [new TextRun({ text: text.toUpperCase(), bold: true, size: 18, font: "Arial", color: GOLD, characterSpacing: 100 })]
  });
}

function p(text, opts = {}) {
  return new Paragraph({
    spacing: { after: opts.afterSpacing || 120 },
    children: [new TextRun({ text, size: 22, font: "Arial", color: opts.color || GRAY, bold: opts.bold || false, italics: opts.italic || false })]
  });
}

function pBold(text) {
  return p(text, { bold: true, color: DARK });
}

function pAccent(text) {
  return new Paragraph({
    spacing: { before: 200, after: 200 },
    shading: { fill: ACCENT_BG, type: ShadingType.CLEAR },
    children: [new TextRun({ text, size: 24, font: "Arial", color: DARK, bold: true })]
  });
}

function emptyLine() {
  return new Paragraph({ spacing: { after: 80 }, children: [] });
}

function divider() {
  return new Paragraph({
    spacing: { before: 200, after: 200 },
    border: { bottom: { style: BorderStyle.SINGLE, size: 1, color: "DDDDDD" } },
    children: []
  });
}

const cellBorder = { style: BorderStyle.SINGLE, size: 1, color: "CCCCCC" };
const borders = { top: cellBorder, bottom: cellBorder, left: cellBorder, right: cellBorder };
const cellMargins = { top: 80, bottom: 80, left: 120, right: 120 };

function tableCell(text, width, opts = {}) {
  return new TableCell({
    borders,
    width: { size: width, type: WidthType.DXA },
    margins: cellMargins,
    shading: opts.header ? { fill: LIGHT_BG, type: ShadingType.CLEAR } : undefined,
    children: [new Paragraph({
      children: [new TextRun({ text, size: 20, font: "Arial", color: DARK, bold: opts.header || false })]
    })]
  });
}

